func TestGeneratorHandlesAnnotatedPackage(t *testing.T) {
	roots, err := loader.LoadRoots("./testdata/simple")
	if err != nil {
		t.Fatalf("load roots: %v", err)
	}
	if len(roots) != 1 {
		t.Fatalf("expected a single root package, got %d", len(roots))
	}

	registry := &markers.Registry{}
	generator := Generator{}
	if err := generator.RegisterMarkers(registry); err != nil {
		t.Fatalf("register markers: %v", err)
	}

	iocCtx := IocGoGenerationContext{
		Collector: &markers.Collector{Registry: registry},
		Checker:   &loader.TypeChecker{},
	}
	out := iocCtx.handlePackage(roots[0])
	if len(out) == 0 {
		t.Fatalf("expected generated output")
	}
	content := string(out)

	checks := []string{
		"// Code generated by iocgo ctl (ioctl). DO NOT EDIT.",
		"// Generated at:",
		"// Source package:",
		"func RegisterIocgoBeans(reg iocgo.Registry) error",
		"iocgo.Define[*Repo]",
		"iocgo.Define[*AppConfiguration]",
		"iocgo.Define[*Service]",
		"iocgo.RegisterBeans",
	}
	for _, check := range checks {
		if !strings.Contains(content, check) {
			t.Fatalf("expected generated content to contain %q\n%s", check, content)
		}
	}
}

