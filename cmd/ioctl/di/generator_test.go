func TestGeneratorRejectsConfigurationPropertiesWithoutPrefix(t *testing.T) {
	roots, err := loader.LoadRoots("./testdata/invalidcfg")
	if err != nil {
		t.Fatalf("load roots: %v", err)
	}

	registry := &markers.Registry{}
	generator := Generator{}
	if err := generator.RegisterMarkers(registry); err != nil {
		t.Fatalf("register markers: %v", err)
	}

	iocCtx := IocGoGenerationContext{
		Collector: &markers.Collector{Registry: registry},
		Checker:   &loader.TypeChecker{},
	}
	out := iocCtx.handlePackage(roots[0])
	if len(out) != 0 {
		t.Fatalf("expected no generated output for invalid configuration-properties usage")
	}
	if len(roots[0].Errors) == 0 {
		t.Fatalf("expected package errors for missing configuration-properties prefix")
	}
	if !strings.Contains(roots[0].Errors[0].Msg, "configuration-properties") {
		t.Fatalf("expected configuration-properties error, got %+v", roots[0].Errors)
	}
}

